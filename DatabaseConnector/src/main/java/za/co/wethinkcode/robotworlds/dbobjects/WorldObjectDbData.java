package za.co.wethinkcode.robotworlds.dbobjects;

public class WorldObjectDbData {
    private final int width, height, x, y;

    public WorldObjectDbData(int width, int height, int x, int y) {
        this.width = width;
        this.height = height;
        this.x = x;
        this.y = y;
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
